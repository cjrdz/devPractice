# Prompt the user to enter the base number
base_num = int(input("Enter your first number: "))

# Prompt the user to enter the exponent number
power_num = int(input("Enter your second number: "))

# Calculate the result of raising base_num to the power of power_num
result = base_num ** power_num

# Print the result
print(f"The result is: {result}, {base_num}^{power_num}")